package com.example.adetl

class dogInfo {
    var name = ""
    var breed = ""
    var gender = ""
    var age = ""
    var dob = ""

    constructor(name:String, breed:String, gender: String, age:String, dob:String) {
        this.name = name
        this.breed = breed
        this.gender = gender
        this.age = age
        this.dob = dob
    }
}